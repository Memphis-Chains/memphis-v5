#!/usr/bin/env node
import { mkdirSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';
import process from 'node:process';

const outDir = resolve('docs/api');
mkdirSync(outDir, { recursive: true });

const cliCommands = [
  'health',
  'providers:health',
  'providers list',
  'models list',
  'chat',
  'ask',
  'decide history',
  'decide transition',
  'infer',
  'mcp serve|serve-once|serve-status|serve-stop',
  'tui',
  'doctor',
  'onboarding wizard|bootstrap',
  'chain import_json',
  'chain rebuild',
  'vault init|add|get|list',
  'embed store|search|reset',
  'completion bash|zsh|fish',
];

const cliDoc = [
  '# CLI API',
  '',
  'Generated by `node scripts/generate-api-docs.mjs`.',
  '',
  '## Commands',
  ...cliCommands.map((c) => `- ` + '`memphis-v4 ' + c + '`'),
  '',
].join('\n');

const httpDoc = [
  '# HTTP API',
  '',
  'Generated from current server contracts and routes.',
  '',
  '## Endpoints',
  '- `GET /health` - health probe',
  '- `GET /metrics` - Prometheus metrics',
  '- `POST /chat` - chat completion endpoint',
  '- `POST /ask` - ask completion endpoint',
  '- `POST /v1/sessions` - create session',
  '- `GET /v1/sessions/:id` - fetch session',
  '- `POST /v1/sessions/:id/recall` - recall session context',
  '- `POST /v1/vault/init` - initialize vault',
  '- `POST /v1/vault/encrypt` - encrypt data',
  '- `POST /v1/vault/decrypt` - decrypt data',
  '',
  '## Auth / Policies',
  '- Bearer token policy via `src/infra/http/auth-policy.ts`',
  '- Rate limit policy via `src/infra/http/rate-limit.ts`',
  '',
].join('\n');

const typeDoc = [
  '# TypeDoc',
  '',
  'Project uses TypeDoc for TS API surface generation.',
  '',
  '## Generate',
  '- `npm run docs:typedoc`',
  '- Output dir: `docs/api/typedoc`',
  '',
].join('\n');

writeFileSync(resolve(outDir, 'cli.md'), `${cliDoc}\n`, 'utf8');
writeFileSync(resolve(outDir, 'http.md'), `${httpDoc}\n`, 'utf8');
writeFileSync(resolve(outDir, 'typedoc.md'), `${typeDoc}\n`, 'utf8');

process.stdout.write(`${JSON.stringify({ ok: true, outDir, files: ['cli.md', 'http.md', 'typedoc.md'] }, null, 2)}\n`);
